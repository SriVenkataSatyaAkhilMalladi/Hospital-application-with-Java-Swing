    private void CommSearchTextFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_CommSearchTextFieldKeyPressed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) commtable.getModel();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
        commtable.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(CommSearchTextField.getText().trim()));
    }//GEN-LAST:event_CommSearchTextFieldKeyPressed

