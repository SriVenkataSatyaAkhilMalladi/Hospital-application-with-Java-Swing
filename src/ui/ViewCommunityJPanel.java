    private void commtableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_commtableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) commtable.getModel();
        String communityName = model.getValueAt(commtable.getSelectedRow(),0).toString();
        String pincode = model.getValueAt(commtable.getSelectedRow(),1).toString();

        VCommNameTextField.setText(communityName);
        VPincodeTextField.setText(pincode);
    }//GEN-LAST:event_commtableMouseClicked

    private void CommUpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CommUpdateButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) commtable.getModel();
        if(commtable.getSelectedRowCount()==1){
            //Employee selectedEmployee = (Employee) model.getValueAt(emptable.getSelectedRow(),0);
            String communityName = VCommNameTextField.getText();
            //com.setCommunityName(CommNameTextField.getText());
            int pincode = Integer.parseInt(VPincodeTextField.getText());

            model.setValueAt(communityName, commtable.getSelectedRow(), 1);
            model.setValueAt(pincode,commtable.getSelectedRow(),2);

            JOptionPane.showMessageDialog(this,"Updated Successfully!");
        }
        else {
            if(commtable.getRowCount()==0){
                JOptionPane.showMessageDialog(this,"This is an empty table.");
            }
            else{
                JOptionPane.showMessageDialog(this,"Please select atleast one row.");
            }
        }
    }//GEN-LAST:event_CommUpdateButtonActionPerformed

    private void CommDeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CommDeleteButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) commtable.getModel();
        if(commtable.getSelectedRowCount()==1){
            model.removeRow(commtable.getSelectedRow());
            JOptionPane.showMessageDialog(this,"Deleted Successfully.");
        }
        else{
            if(commtable.getRowCount()==0)
            {
                JOptionPane.showMessageDialog(this,"This is an empty table.");
            }
            else{
                JOptionPane.showMessageDialog(this,"Please select atleast one row to delete.");
            }
        }
    }//GEN-LAST:event_CommDeleteButtonActionPerformed

