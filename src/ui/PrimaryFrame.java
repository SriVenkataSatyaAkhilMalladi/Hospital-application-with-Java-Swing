    private void SignInBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignInBtnActionPerformed
        // TODO add your handling code here:
        String username = UserName.getText();
        String password = Password.getText();
        String selectuser = SelectUserCB.getSelectedItem().toString();

        if(username.equals("Enter Username") || password.equals("Enter Password"))
        {
            JOptionPane.showMessageDialog(rootPane,"Enter Username/Password","Error",1);
        }
        else {
            try{
                switch (SelectUserCB.getSelectedIndex()) {
                    case 0:
                    if(username.equals("Patient") && password.equals("Patient123"))
                    {
                        PatientJFrame patientFrame = new PatientJFrame();
                        patientFrame.setVisible(true);
                        this.setVisible(false);
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this,"Invalid Credentials");
                    }   break;
                    case 1:
                    if(username.equals("Doctor") && password.equals("Doctor123"))
                    {
                        DoctorJFrame doctorFrame = new DoctorJFrame();
                        doctorFrame.setVisible(true);
                        this.setVisible(false);
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this,"Invalid Credentials");
                    }   break;
                    case 2:
                    if(username.equals("Hospital") && password.equals("Hospital123"))
                    {
                        HospitalJFrame hospitalFrame = new HospitalJFrame();
                        hospitalFrame.setVisible(true);
                        this.setVisible(false);
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this,"Invalid Credentials");
                    }   break;
                    case 3:
                    if(username.equals("Community") && password.equals("Community123"))
                    {
                        CommunityJFrame communityFrame = new CommunityJFrame();
                        communityFrame.setVisible(true);
                        this.setVisible(false);
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this,"Invalid Credentials");
                    }   break;
                    case 4:
                    if(username.equals("SysAdmin") && password.equals("SysAdmin123"))
                    {
                        SysAdminJFrame sysadminFrame = new SysAdminJFrame();
                        sysadminFrame.setVisible(true);
                        this.setVisible(false);
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this,"Invalid Credentials");
                    }   break;
                    default:
                    break;
                }
            }
            catch(Exception ex){
                System.out.print(""+ex);
            }

        }
    }//GEN-LAST:event_SignInBtnActionPerformed

