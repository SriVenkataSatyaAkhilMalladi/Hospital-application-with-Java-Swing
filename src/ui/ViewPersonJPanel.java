    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if(jTable1.getSelectedRowCount()==1){
            //Employee selectedEmployee = (Employee) model.getValueAt(emptable.getSelectedRow(),0);
            String name = jTextField1.getText();
            int age = Integer.parseInt(jTextField2.getText());
            String gender = jTextField3.getText();
            String role = jTextField4.getText();
            String community = jTextField5.getText();
            String address = jTextField6.getText();
            long contact = Long.parseLong(jTextField7.getText());

            model.setValueAt(name, jTable1.getSelectedRow(), 0);
            model.setValueAt(age,jTable1.getSelectedRow(),1);
            model.setValueAt(gender,jTable1.getSelectedRow(),2);
            model.setValueAt(role,jTable1.getSelectedRow(),3);
            model.setValueAt(community,jTable1.getSelectedRow(),4);
            model.setValueAt(address,jTable1.getSelectedRow(),5);
            model.setValueAt(contact,jTable1.getSelectedRow(),6);

            JOptionPane.showMessageDialog(this,"Updated Successfully!");
        }
        else {
            if(jTable1.getRowCount()==0){
                JOptionPane.showMessageDialog(this,"This is an empty table.");
            }
            else{
                JOptionPane.showMessageDialog(this,"Please select atleast one row.");
            }
        }
        }

        private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {
            // TODO add your handling code here:
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            String name = model.getValueAt(jTable1.getSelectedRow(),0).toString();
            String age = model.getValueAt(jTable1.getSelectedRow(),1).toString();
            String gender = model.getValueAt(jTable1.getSelectedRow(),2).toString();
            String role = model.getValueAt(jTable1.getSelectedRow(),3).toString();
            String community = model.getValueAt(jTable1.getSelectedRow(),4).toString();
            String address = model.getValueAt(jTable1.getSelectedRow(),5).toString();
            String contact = model.getValueAt(jTable1.getSelectedRow(),6).toString();

            jTextField1.setText(name);
            jTextField2.setText(age);
            jTextField3.setText(gender);
            jTextField4.setText(role);
            jTextField5.setText(community);
            jTextField6.setText(address);
            jTextField7.setText(contact);
    }//GEN-LAST:event_jButton1ActionPerformed

